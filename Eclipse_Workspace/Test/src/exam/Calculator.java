package exam;

public class Calculator {
	int add(int x, int y) {
		int result = x+y;
		return result;
	}
	int sub (int x, int y) {
		int result = x-y;
		return result;
	}
	int mul(int x, int y) {
		int result = x*y;
		return result;
	}
	double div( double x, double y) {
		double result = x / y;
		return result;
	}
}
